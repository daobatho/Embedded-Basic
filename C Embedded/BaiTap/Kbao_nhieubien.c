#include <stdio.h>

int main() {
    // Khai báo và định nghĩa nhiều biến
    int integerVar = 20;         // Biến số nguyên
    float floatVar = 3.14f;      // Biến số thực
    char charVar = 'A';          // Biến ký tự

    // In ra giá trị của từng biến
    printf("Giá trị của biến số nguyên là: %d\n", integerVar);
    printf("Giá trị của biến số thực là: %.2f\n", floatVar);
    printf("Giá trị của biến ký tự là: %c\n", charVar);

    return 0;
}
